package com.jony.star.pattern;

public class SquareFillPattern {

	public static void main(String[] args) {
		int row=6;
		int star=6;
		
		for (int i = 1; i <=row; i++) {
			for (int s = 1; s <=star; s++) {
				System.out.print(" *");
				
			}
			System.out.println();
			
			
		}
		

	}

}
